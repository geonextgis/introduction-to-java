public class Ex10_Write_Expression {
    public static void main(String[] args) {
        float v = 4;
        float u = 2;
        float a = 5;
        float s = 8;

        float expression = (v * v - u * u) / (2 * a * s);
        System.out.println("The value of the expression is: " + expression);
    }
}
